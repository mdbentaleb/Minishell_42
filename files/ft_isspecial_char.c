int	is_special_char(char c)
{
	return (c == '-' || c == '=' || c == '@' || c == '+' || c == ':'
		|| c == ',' || c == '.' || c == '/' || c == '?' || c == '['
		|| c == ']' || c == '{' || c == '}' || c == '~' || c == '$'
		|| c == '%' || c == '#' || c == '>' || c == '<' || c == '^'
		|| c == '\''
	);
}

int	ft_is_special_char(char c)
{
	if (c == '-' || c == '=' || c == '@' || c == '+' || c == ':'
		|| c == ',' || c == '.' || c == '/' || c == '?' || c == '['
		|| c == ']' || c == '{' || c == '}' || c == '~' || c == '$'
		|| c == '%' || c == '#' || c == '>' || c == '<' || c == '^')
		return (1);
	return (0);
}

int	ft_if_should_skip(char c)
{
	if (c == '\'' || c == '"' || c == '\\' || c == '('
		|| c == '{' || c == ' ' || c == '\0')
		return (1);
	return (0);
}
